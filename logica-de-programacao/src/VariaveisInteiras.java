public class VariaveisInteiras {
    public static void main (String[] args){
        byte idade1 = 20;
        short idade2 = 21;
        int idade3 = 22;
        long idade4 = 23;

        System.out.println("O valor da idade em byte é: " +idade1);
        System.out.println("O valor da idade em short é: " +idade2);
        System.out.println("O valor da idade em int é: " +idade3);
        System.out.println("O valor da idade em long é: " +idade4);
    }
}
